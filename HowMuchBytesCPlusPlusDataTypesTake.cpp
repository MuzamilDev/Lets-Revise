#include<iostream>
using namespace std;

int main()
{

    cout<<"The Char takes: "<<sizeof(char)<<" bytes"<<endl;
    cout<<"The Boolean takes: "<<sizeof(bool)<<" bytes"<<endl;
    cout<<"The Int takes: "<<sizeof(int)<<" bytes"<<endl;
    cout<<"The Int short takes: "<<sizeof(short)<<" bytes"<<endl;
    cout<<"The Float takes: "<<sizeof(float)<<" bytes"<<endl;
    cout<<"The Long takes: " <<sizeof(long)<<" bytes"<<endl;
    cout<<"The Long Double takes: "<<sizeof(double)<<" bytes"<<endl;
    return 0;
}
